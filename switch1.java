import java.util.*;
public class switch1{
    public static void main(String[] args) {
        Scanner sc= new Scanner(System.in);
        int day= sc.nextInt();
        switch(day){
            case(6):
            System.out.println("Holiday");
            break;
            case(7):
            System.out.println("Holiday");
            break;
            default:
            System.out.println("Not a holiday");
        }
    }
}
